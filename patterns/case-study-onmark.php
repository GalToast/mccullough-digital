<div class="wp-block-group alignwide"><!-- wp:heading {"level":3} -->
<h3>Problem → Fix → Result</h3>
<!-- /wp:heading -->

<!-- wp:columns -->
<div class="wp-block-columns"><!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading {"level":4} -->
<h4>Problem</h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Starting from zero: no website, no active social, no tracking, and no single “start here” path. Local visibility was limited; inquiries were sporadic and unattributed.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading {"level":4} -->
<h4>Fix</h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><strong>Build:</strong> WP block theme with fast “Get Pricing &amp; Timeline” + mobile contact bar. <strong>Conversion &amp; first-contact:</strong> Custom lead form, instant auto-reply, internal ping, pipeline stage, templated estimate follow-up. <strong>Sales enablement:</strong> Searchable 400+ swatch gallery with filters and “attach to inquiry.” <strong>Content:</strong> Seeded blog (several posts). <strong>Measure:</strong> GA4+GTM (form_submit, click_to_call, cta_click) + UTMs. <strong>Be found:</strong> GBP creation/management, on-page SEO, speed pass, schema &amp; entity signals. <strong>Paid:</strong> Google Ads (Search+PMAX), Meta, Nextdoor; <strong>LSA</strong> approval &amp; activation. <strong>Social:</strong> FB/IG/Nextdoor profiles. <strong>Ops:</strong> 30/90/180/365 follow-ups; consolidated access.</p>
<!-- /wp:paragraph --></div>
<!-- /wp:column -->

<!-- wp:column -->
<div class="wp-block-column"><!-- wp:heading {"level":4} -->
<h4>Result</h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Predictable pipeline: ~35–45 forms in 60 days + ~2 calls/day, Top-5 for the primary Houston term, paid assist ~12–16 ad-leads/mo on $800/mo + LSA 5–10/mo, and clean attribution. Prospects self-select via the swatch gallery; blog posts support long-tail; OnMark surfaces in Gemini/ChatGPT for “solid surface fabricators in Houston.”</p>
